     /**
     * Salva as alterações ou o novo Produto.
     * @param evt 
     */
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        produto = new Produto();
        setProduto(produto);
        try{
            if (isIns){
                f.incluir(produto);
            }else{
                f.alterar(produto);
            }
            doClose(RET_OK);
        }catch(GeralException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }                              
